/**
 * Generate SEO keywords for a product based on its properties
 */
export function generateProductKeywords(product: {
  brand: string;
  horsepower: number;
  engineType?: string;
  type?: string;
  applicationTypes?: string[];
}): string[] {
  const keywords: string[] = [];
  
  // Brand keywords
  const brandLower = product.brand.toLowerCase();
  if (AI_SEARCH_KEYWORDS.brands[brandLower as keyof typeof AI_SEARCH_KEYWORDS.brands]) {
    keywords.push(...AI_SEARCH_KEYWORDS.brands[brandLower as keyof typeof AI_SEARCH_KEYWORDS.brands]);
  }
  
  // Horsepower keywords
  if (product.horsepower) {
    keywords.push(`${product.horsepower} hp outboard`);
    keywords.push(`${product.horsepower} horsepower outboard motor`);
    keywords.push(`${product.horsepower}hp ${product.brand} outboard`);
    
    if (product.horsepower <= 20) {
      keywords.push(...AI_SEARCH_KEYWORDS.horsepower.portable);
    } else if (product.horsepower <= 90) {
      keywords.push(...AI_SEARCH_KEYWORDS.horsepower.midRange);
    } else {
      keywords.push(...AI_SEARCH_KEYWORDS.horsepower.highPower);
    }
  }
  
  // Engine type keywords
  if (product.engineType) {
    if (product.engineType.includes('2-Stroke')) {
      keywords.push(...AI_SEARCH_KEYWORDS.engineType.twoStroke);
    } else if (product.engineType.includes('4-Stroke')) {
      keywords.push(...AI_SEARCH_KEYWORDS.engineType.fourStroke);
    } else if (product.engineType.includes('Electric')) {
      keywords.push(...AI_SEARCH_KEYWORDS.engineType.electric);
    }
  }
  
  // Application keywords
  if (product.applicationTypes) {
    product.applicationTypes.forEach(app => {
      const appLower = app.toLowerCase();
      if (appLower.includes('fish')) {
        keywords.push(...AI_SEARCH_KEYWORDS.applications.fishing);
      } else if (appLower.includes('pontoon') || appLower.includes('recreation')) {
        keywords.push(...AI_SEARCH_KEYWORDS.applications.recreational);
      } else if (appLower.includes('commercial')) {
        keywords.push(...AI_SEARCH_KEYWORDS.applications.commercial);
      } else if (appLower.includes('sail')) {
        keywords.push(...AI_SEARCH_KEYWORDS.applications.sailing);
      }
    });
  }
  
  // Add general purchase intent keywords
  keywords.push(...AI_SEARCH_KEYWORDS.purchase);
  
  // Add location keywords
  keywords.push(...AI_SEARCH_KEYWORDS.location);
  
  // Remove duplicates and return
  return [...new Set(keywords)];
}

